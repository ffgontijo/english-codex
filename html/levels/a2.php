<section class="section paper" id="conteudos-a2">
  <div class="container">
    <div class="section-head">
      <h2>Conteúdos essenciais do A2</h2>
      <p>
        O A2 amplia a base do A1. Aqui o aluno começa a narrar, planejar, comparar e explicar pequenas situações. Não é fluência plena ainda, mas já é comunicação de verdade, essa coisa rara e trabalhosa.
      </p>
    </div>

    <div class="grammar-block">
      <h3>1 · Past Simple: eventos concluídos</h3>

      <p>
        O <em>Past Simple</em> é usado para falar de ações terminadas no passado. Ele aparece com expressões como <em>yesterday</em>, <em>last week</em>, <em>in 2020</em>, <em>two days ago</em> e <em>when I was younger</em>.
      </p>

      <div class="formula">
        Subject + past verb
      </div>

      <h4>Exemplos</h4>
      <ul class="ex-list">
        <li><em>I watched a movie yesterday.</em></li>
        <li><em>She visited her grandmother last weekend.</em></li>
        <li><em>The detective found a clue in the old library.</em></li>
        <li><em>The crew landed on a frozen planet.</em></li>
        <li><em>We played a board game on Saturday.</em></li>
      </ul>

      <div class="tip">
        <strong>Regulares:</strong> muitos verbos recebem <em>-ed</em>: <em>work → worked</em>, <em>visit → visited</em>, <em>play → played</em>.
      </div>

      <div class="warn">
        <strong>Português te trai aqui:</strong> não use o verbo no passado depois de <em>did</em>. Diga <em>Did you watch?</em>, não <em>Did you watched?</em>. O <em>did</em> já carrega o passado, coitado, deixe ele trabalhar.
      </div>
    </div>

    <div class="grammar-block">
      <h3>2 · Verbos irregulares</h3>

      <p>
        Alguns verbos não seguem a regra do <em>-ed</em>. Eles têm formas próprias no passado. A solução é aprender em grupos pequenos, com frases, e não tentar decorar uma lista de 200 verbos como se fosse penitência medieval.
      </p>

      <div class="table-wrap">
        <table>
          <caption>Verbos irregulares úteis no A2</caption>
          <thead>
            <tr>
              <th>Base form</th>
              <th>Past Simple</th>
              <th>Exemplo</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>go</td>
              <td>went</td>
              <td><em>I went to the supermarket yesterday.</em></td>
            </tr>
            <tr>
              <td>see</td>
              <td>saw</td>
              <td><em>We saw a great movie last night.</em></td>
            </tr>
            <tr>
              <td>buy</td>
              <td>bought</td>
              <td><em>She bought a new notebook.</em></td>
            </tr>
            <tr>
              <td>make</td>
              <td>made</td>
              <td><em>He made a mistake.</em></td>
            </tr>
            <tr>
              <td>take</td>
              <td>took</td>
              <td><em>They took the wrong bus.</em></td>
            </tr>
            <tr>
              <td>write</td>
              <td>wrote</td>
              <td><em>I wrote a short story.</em></td>
            </tr>
