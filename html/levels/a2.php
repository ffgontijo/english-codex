              <td>had</td>
              <td><em>We had lunch at noon.</em></td>
            </tr>
          </tbody>
        </table>
      </div>

      <div class="tip">
        <strong>Para o caderno:</strong> aprenda 5 verbos irregulares por semana, sempre com frases suas.
      </div>
    </div>

    <div class="grammar-block">
      <h3>3 · Past Simple: perguntas e negativas</h3>

      <p>
        Para perguntas e negativas no passado, usamos <em>did</em> e <em>didn't</em>. O verbo principal volta para a forma base.
      </p>

      <div class="formula">
        Did + subject + base verb? | Subject + didn't + base verb
      </div>

      <h4>Exemplos</h4>
      <ul class="ex-list">
        <li><em>Did you watch the episode?</em></li>
        <li><em>Did she call you?</em></li>
        <li><em>They didn't finish the mission.</em></li>
        <li><em>I didn't understand the instructions.</em></li>
        <li><em>The wizard didn't open the door.</em></li>
      </ul>

      <div class="warn">
        <strong>Erro comum:</strong> <em>Did you went?</em> está errado. O correto é <em>Did you go?</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>4 · There was / There were</h3>

      <p>
        Use <em>there was</em> e <em>there were</em> para falar que algo existia ou aconteceu em algum lugar no passado.
      </p>

      <div class="formula">
        There was + singular | There were + plural
      </div>

      <ul class="ex-list">
        <li><em>There was a problem with the system.</em></li>
        <li><em>There was a strange noise in the corridor.</em></li>
        <li><em>There were three people in the room.</em></li>
        <li><em>There were many books on the table.</em></li>
      </ul>

      <div class="tip">
        <strong>Português ajuda:</strong> pense em “havia” ou “tinha” no sentido de existência. Mas em inglês, use <em>there was</em> ou <em>there were</em>, não <em>had</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>5 · Future with going to</h3>

      <p>
        Use <em>going to</em> para falar de planos, intenções e previsões baseadas em sinais visíveis.
      </p>

      <div class="formula">
        Subject + am / is / are + going to + base verb
      </div>

      <ul class="ex-list">
        <li><em>I am going to study tonight.</em></li>
        <li><em>She is going to travel next month.</em></li>
        <li><em>We are going to watch a movie on Friday.</em></li>
        <li><em>The team is going to investigate the signal.</em></li>
        <li><em>Look at those clouds. It is going to rain.</em></li>
      </ul>

      <div class="warn">
        <strong>Português te trai aqui:</strong> não diga <em>I go to study tomorrow</em> para plano futuro. Use <em>I am going to study tomorrow</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>6 · Future with will</h3>

      <p>
        Use <em>will</em> para decisões no momento da fala, promessas, ofertas e previsões gerais.
      </p>

      <div class="formula">
        Subject + will + base verb
      </div>

      <ul class="ex-list">
        <li><em>I will help you.</em></li>
        <li><em>I think the movie will be interesting.</em></li>
        <li><em>She will probably arrive late.</em></li>
        <li><em>The hero will need a better plan.</em></li>
        <li><em>Don't worry. I will send the file.</em></li>
      </ul>

      <div class="tip">
        <strong>Diferença inicial:</strong> <em>going to</em> soa mais como plano. <em>will</em> soa mais como decisão, previsão ou promessa.
      </div>
    </div>

    <div class="grammar-block">
      <h3>7 · Comparatives and superlatives</h3>

      <p>
        Use comparativos para comparar duas coisas e superlativos para destacar uma coisa dentro de um grupo.
      </p>

      <div class="formula">
        adjective + -er + than | more + adjective + than | the + adjective + -est | the most + adjective
      </div>

      <h4>Comparativos</h4>
      <ul class="ex-list">
        <li><em>This book is shorter than the first one.</em></li>
        <li><em>The second movie is better than the first.</em></li>
        <li><em>This route is more dangerous than the old road.</em></li>
        <li><em>My city is smaller than São Paulo.</em></li>
      </ul>

      <h4>Superlativos</h4>
      <ul class="ex-list">
        <li><em>This is the best episode of the season.</em></li>
        <li><em>She is the smartest person in the room.</em></li>
        <li><em>It was the most difficult mission.</em></li>
      </ul>

      <div class="warn">
        <strong>Erro comum:</strong> não diga <em>more better</em>. Use apenas <em>better</em>. O inglês já sofreu o suficiente.
      </div>
    </div>

    <div class="grammar-block">
      <h3>8 · Countable and uncountable nouns</h3>

      <p>
        Alguns substantivos podem ser contados: <em>book, apple, chair</em>. Outros normalmente não são contados diretamente: <em>water, money, information, advice</em>.
      </p>

      <div class="table-wrap">
        <table>
          <caption>Countable vs uncountable</caption>
          <thead>
            <tr>
              <th>Tipo</th>
              <th>Exemplos</th>
              <th>Como usar</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Countable</td>
              <td><em>book, chair, idea, question, apple</em></td>
              <td><em>one book, two chairs, many questions</em></td>
            </tr>
            <tr>
              <td>Uncountable</td>
              <td><em>water, money, information, advice, homework</em></td>
              <td><em>some water, much money, a piece of advice</em></td>
            </tr>
          </tbody>
        </table>
      </div>

      <div class="warn">
        <strong>Português te trai aqui:</strong> <em>information</em> e <em>advice</em> geralmente não vão para o plural. Diga <em>some information</em> e <em>a piece of advice</em>, não <em>informations</em> e <em>advices</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>9 · Quantifiers: some, any, much, many, a lot of</h3>

      <p>
        Quantifiers indicam quantidade. Eles aparecem muito em compras, comida, estudos, tarefas, viagem e rotina.
      </p>

      <ul class="ex-list">
        <li><em>I have some questions.</em></li>
        <li><em>Do you have any water?</em></li>
        <li><em>There isn't much time.</em></li>
        <li><em>There are many people here.</em></li>
        <li><em>She has a lot of books.</em></li>
      </ul>

      <div class="tip">
        <strong>Regra inicial:</strong> use <em>many</em> com contáveis no plural e <em>much</em> com incontáveis. Para simplificar em fala comum, <em>a lot of</em> ajuda bastante.
      </div>
    </div>

    <div class="grammar-block">
      <h3>10 · Modal verbs: can, should, must and have to</h3>

      <p>
        Modais ajudam a falar de habilidade, permissão, conselho, obrigação e necessidade.
      </p>

      <div class="table-wrap">
        <table>
          <caption>Modais essenciais no A2</caption>
          <thead>
            <tr>
              <th>Modal</th>
              <th>Função</th>
              <th>Exemplo</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>can</td>
              <td>habilidade ou permissão</td>
              <td><em>I can speak a little English.</em></td>
            </tr>
            <tr>
              <td>should</td>
              <td>conselho</td>
              <td><em>You should review this lesson.</em></td>
            </tr>
            <tr>
              <td>must</td>
              <td>obrigação forte</td>
              <td><em>You must wear a badge here.</em></td>
            </tr>
            <tr>
              <td>have to</td>
              <td>obrigação externa ou necessidade</td>
              <td><em>I have to work tomorrow.</em></td>
            </tr>
          </tbody>
        </table>
      </div>

      <div class="warn">
        <strong>Importante:</strong> depois de modal, o verbo fica na forma base: <em>She can swim</em>, não <em>She can swims</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>11 · Prepositions of time and place</h3>

      <p>
        Preposições aparecem o tempo todo. O problema é que elas raramente seguem tradução literal. Pequenas palavras, grandes tragédias.
      </p>

      <div class="cards two-col">
        <article class="card">
          <h3>Time</h3>
          <ul class="check-list">
            <li><em>at 8 o'clock</em></li>
            <li><em>on Monday</em></li>
            <li><em>in May</em></li>
            <li><em>in 2025</em></li>
            <li><em>at night</em></li>
          </ul>
        </article>

        <article class="card">
          <h3>Place</h3>
          <ul class="check-list">
            <li><em>at home</em></li>
            <li><em>at work</em></li>
            <li><em>in Brazil</em></li>
            <li><em>on the table</em></li>
            <li><em>next to the door</em></li>
          </ul>
        </article>
      </div>

      <div class="tip">
        <strong>Para o caderno:</strong> crie uma página só para preposições com exemplos seus. Não tente aprender todas de uma vez, a não ser que você goste de sofrer com eficiência.
      </div>
    </div>

    <div class="grammar-block">
      <h3>12 · Giving directions</h3>

      <p>
        No A2, o aluno precisa pedir e dar direções simples. Isso é útil em viagens, jogos, mapas, cidades, eventos e situações do cotidiano.
      </p>

      <ul class="ex-list">
        <li><em>Excuse me, where is the train station?</em></li>
        <li><em>Go straight ahead.</em></li>
        <li><em>Turn left at the traffic light.</em></li>
        <li><em>Turn right after the supermarket.</em></li>
        <li><em>It is next to the bank.</em></li>
        <li><em>It is opposite the museum.</em></li>
      </ul>

      <div class="tip">
        <strong>Missão:</strong> descreva o caminho da sua casa até um mercado, escola, trabalho ou lugar conhecido.
      </div>
    </div>
