    <div class="grammar-block">
      <h3>5 · Articles: a, an, the</h3>
      <p>
        Artigos ajudam a indicar se falamos de algo geral ou específico. No A0, o aluno precisa reconhecer o básico.
      </p>

      <ul class="ex-list">
        <li><em>a book</em> - um livro</li>
        <li><em>an apple</em> - uma maçã</li>
        <li><em>the book</em> - o livro específico</li>
        <li><em>a wizard</em> - um mago qualquer</li>
        <li><em>the wizard</em> - o mago que já sabemos quem é</li>
      </ul>

      <div class="tip">
        <strong>Regra inicial:</strong> use <em>a</em> antes de som de consoante e <em>an</em> antes de som de vogal: <em>a hero</em>, <em>an old map</em>.
      </div>
    </div>

