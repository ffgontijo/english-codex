                <th>Forma nominalizada</th>
                <th>Exemplo</th>
              </tr>
            </thead>

            <tbody>
              <tr>
                <td><em>The team implemented the system.</em></td>
                <td><em>The implementation of the system...</em></td>
                <td><em>The implementation of the system reduced manual errors.</em></td>
              </tr>

              <tr>
                <td><em>Students improve gradually.</em></td>
                <td><em>Gradual improvement...</em></td>
                <td><em>Gradual improvement depends on consistent practice.</em></td>
              </tr>

              <tr>
                <td><em>The company decided to change the process.</em></td>
                <td><em>The decision to change the process...</em></td>
                <td><em>The decision to change the process was widely supported.</em></td>
              </tr>

              <tr>
                <td><em>The author criticises society.</em></td>
                <td><em>The author’s criticism of society...</em></td>
                <td><em>The author’s criticism of society becomes clearer in the final chapter.</em></td>
              </tr>
            </tbody>
          </table>
        </div>

        <div class="warn">
          <strong>Cuidado:</strong> nominalisation ajuda em registro formal, mas excesso cria texto pesado.
          Use para precisão, não para parecer um memorando sem alma.
        </div>
      </div>

      <div class="grammar-block">
        <h3>6 · Advanced passive and reporting structures</h3>

        <p>
          No C1, a voz passiva também aparece em estruturas impessoais, muito comuns em escrita
          formal, notícias, relatórios e argumentação acadêmica.
        </p>

        <div class="formula">
          It is believed that... | It has been suggested that... | The issue is thought to be...
        </div>

        <h4>Exemplos</h4>

        <ul class="ex-list">
          <li><em>It is widely believed that feedback plays a key role in learning.</em></li>
          <li><em>It has been suggested that the results were influenced by external factors.</em></li>
          <li><em>The policy is thought to have improved access to education.</em></li>
          <li><em>The painting is believed to have been created in the late nineteenth century.</em></li>
          <li><em>The character is said to represent the author’s fear of modernity.</em></li>
        </ul>

        <div class="tip">
          <strong>Uso acadêmico:</strong> essas estruturas ajudam a apresentar ideias sem depender de
          “people say” ou “everyone thinks”, que soam vagos demais.
        </div>
      </div>

      <div class="grammar-block">
        <h3>7 · Participle clauses</h3>

        <p>
          <em>Participle clauses</em> deixam frases mais compactas e sofisticadas. Elas aparecem muito
          em textos formais, narrativos e jornalísticos.
        </p>

        <div class="formula">
          Having + past participle | verb + -ing | past participle
        </div>

        <h4>Exemplos</h4>

        <ul class="ex-list">
          <li><em>Having reviewed the data, the team decided to change the process.</em></li>
          <li><em>Walking through the abandoned station, the detective noticed fresh footprints.</em></li>
          <li><em>Written in a deliberately simple style, the novel hides a complex political argument.</em></li>
          <li><em>Having failed to prepare properly, the group struggled during the presentation.</em></li>
          <li><em>Surrounded by fog, the village seemed completely isolated.</em></li>
        </ul>

        <div class="warn">
          <strong>Cuidado:</strong> o sujeito precisa fazer sentido. Frases mal montadas criam ambiguidade,
          e aí o texto começa a tropeçar em si mesmo.
        </div>
      </div>

      <div class="grammar-block">
        <h3>8 · Advanced conditionals</h3>

        <p>
          No C1, conditionals não servem só para “if”. Você trabalha alternativas como
          <em>provided that</em>, <em>as long as</em>, <em>unless</em>, <em>otherwise</em> e
          <em>in case</em>.
        </p>

