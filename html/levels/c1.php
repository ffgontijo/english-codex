      <div class="grammar-block">
        <h3>13 · Subtext and implication</h3>

        <p>
          No C1, você começa a perceber o que não foi dito diretamente. Subtexto aparece em literatura,
          filmes, reuniões, entrevistas, e-mails educados demais e qualquer conversa humana onde ninguém
          diz o que realmente quer dizer, essa tradição cansativa.
        </p>

        <div class="cards two-col">
          <article class="card">
            <h3>Frase direta</h3>
