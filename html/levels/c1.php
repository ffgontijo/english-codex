      <div class="grammar-block">
        <h3>9 · Concession and contrast</h3>

        <p>
          C1 exige capacidade de reconhecer e construir contraste sofisticado. Não basta usar
          <em>but</em> como se fosse fita adesiva para qualquer rachadura no texto.
        </p>

