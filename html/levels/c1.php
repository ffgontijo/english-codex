      <div class="section-head">
        <h2>Conteúdos essenciais do C1</h2>
        <p>
          O C1 é onde o estudante deixa de perguntar apenas “isso está certo?” e começa a perguntar
          “isso soa natural, preciso, adequado e eficaz?”. Bem-vindo ao nível em que a gramática
          vira estratégia.
        </p>
      </div>

      <div class="grammar-block">
        <h3>1 · Register: formal, neutral and informal language</h3>

        <p>
          Registro é a escolha de linguagem conforme contexto, público e objetivo. No C1, você precisa
          reconhecer quando uma frase soa informal demais, formal demais, direta demais ou vaga demais.
        </p>

        <div class="table-wrap">
          <table>
            <caption>Registro em inglês</caption>

            <thead>
              <tr>
                <th>Intenção</th>
                <th>Informal</th>
                <th>Neutro/Formal</th>
              </tr>
            </thead>

            <tbody>
              <tr>
                <td>Pedir ajuda</td>
                <td><em>Can you help me with this?</em></td>
                <td><em>I would appreciate your assistance with this matter.</em></td>
              </tr>

              <tr>
                <td>Discordar</td>
                <td><em>I don’t really agree.</em></td>
                <td><em>I would take a slightly different view.</em></td>
              </tr>

              <tr>
                <td>Explicar problema</td>
                <td><em>There’s a problem with the file.</em></td>
                <td><em>An issue has been identified with the file.</em></td>
              </tr>

              <tr>
                <td>Sugerir</td>
                <td><em>Maybe we should change it.</em></td>
                <td><em>It may be worth considering an alternative approach.</em></td>
              </tr>
            </tbody>
          </table>
        </div>

        <div class="tip">
          <strong>Para o caderno:</strong> escolha uma frase simples e reescreva em três registros:
          informal, profissional e acadêmico.
        </div>
      </div>

      <div class="grammar-block">
        <h3>2 · Hedging: linguagem cautelosa e precisa</h3>

        <p>
          <em>Hedging</em> serve para evitar afirmações absolutas, especialmente em escrita acadêmica,
          relatórios, propostas e argumentação. Não é insegurança. É precisão. O mundo seria menos
          insuportável se mais gente entendesse isso.
        </p>

        <div class="formula">
          It could be argued that... | This appears to suggest... | To some extent...
        </div>

        <h4>Exemplos</h4>

        <ul class="ex-list">
          <li><em>It could be argued that the policy has produced mixed results.</em></li>
          <li><em>The evidence appears to suggest a connection between motivation and progress.</em></li>
          <li><em>To some extent, online learning depends on the student’s ability to self-regulate.</em></li>
          <li><em>This may indicate a broader issue with the way the process was implemented.</em></li>
          <li><em>There is some evidence to suggest that regular feedback improves performance.</em></li>
        </ul>

        <div class="warn">
          <strong>Erro comum:</strong> C1 não significa soar arrogante. Frases absolutas demais podem
          parecer fracas porque ignoram complexidade.
        </div>
      </div>

      <div class="grammar-block">
        <h3>3 · Inversion for emphasis</h3>

        <p>
          Inversões dão ênfase e deixam a frase mais formal ou dramática. Elas aparecem em essays,
          literatura, speeches, artigos e textos sofisticados. Use com cuidado, não como glitter
          gramatical.
        </p>

        <div class="formula">
          Negative / limiting expression + auxiliary + subject + verb
        </div>

        <h4>Exemplos</h4>

        <ul class="ex-list">
          <li><em>Never have I seen such a convincing argument.</em></li>
          <li><em>Rarely does a single scene change the meaning of an entire story.</em></li>
          <li><em>Not only did the project fail, but it also exposed deeper organisational problems.</em></li>
          <li><em>Only after the final chapter do we understand the narrator’s real intention.</em></li>
          <li><em>No sooner had the meeting started than the system crashed.</em></li>
        </ul>

        <div class="tip">
          <strong>Uso real:</strong> ótimo para escrita formal e análise crítica, mas estranho em fala
          cotidiana se usado demais.
        </div>
      </div>

      <div class="grammar-block">
        <h3>4 · Cleft sentences</h3>

        <p>
          <em>Cleft sentences</em> destacam uma parte específica da frase. Elas ajudam a dar foco,
          contraste e ênfase.
        </p>

        <div class="formula">
          What... is... | It is / was... that...
        </div>

        <h4>Exemplos</h4>

        <ul class="ex-list">
          <li><em>What matters most is not the tool itself, but how it is used.</em></li>
          <li><em>What surprised me was the ending.</em></li>
          <li><em>It was the lack of planning that caused the delay.</em></li>
          <li><em>It is the character’s silence that makes the scene so powerful.</em></li>
          <li><em>What the report shows is a need for clearer communication.</em></li>
        </ul>

        <div class="tip">
          <strong>Para speaking:</strong> use cleft sentences para ganhar controle e destacar sua ideia
          principal.
        </div>
      </div>

      <div class="grammar-block">
        <h3>5 · Nominalisation</h3>

        <p>
          Nominalisation transforma ações em substantivos. Isso deixa textos mais formais, densos
          e acadêmicos. Também pode deixar tudo morto e burocrático se você exagerar, porque humanos
          sempre encontram uma forma de estragar uma ferramenta útil.
        </p>

        <div class="table-wrap">
          <table>
            <caption>Verbo para substantivo</caption>

            <thead>
              <tr>
