      <div class="grammar-block">
        <h3>12 · Adverbs of frequency</h3>

        <p>
          Adverbs of frequency mostram com que frequência algo acontece. Eles são perfeitos para falar de rotina.
        </p>

        <div class="formula">
          always → usually → often → sometimes → rarely → never
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I always drink coffee in the morning.</em></li>
          <li><em>She usually studies at night.</em></li>
          <li><em>We sometimes play board games.</em></li>
          <li><em>He never watches horror movies.</em></li>
        </ul>

        <div class="tip">
          <strong>Posição comum:</strong> antes do verbo principal: <em>I usually study</em>. Depois do <em>to be</em>: <em>She is always tired</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>13 · Can / Can't</h3>

        <p>
          Use <em>can</em> para falar de habilidade, possibilidade ou permissão simples.
        </p>

        <div class="formula">
          Subject + can / can't + base verb
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I can speak a little English.</em></li>
          <li><em>She can play the guitar.</em></li>
          <li><em>We can study together.</em></li>
          <li><em>He can't drive.</em></li>
          <li><em>The character can open the secret door.</em></li>
        </ul>

        <div class="warn">
          <strong>Importante:</strong> depois de <em>can</em>, o verbo não recebe <em>-s</em>: <em>She can speak</em>, não <em>She can speaks</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>14 · Likes and preferences</h3>

        <p>
          No A1, o aluno precisa falar do que gosta, não gosta e prefere. Isso gera conversa real rapidamente.
        </p>

        <div class="formula">
          like / love / hate + noun | like / love / hate + verb + -ing
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I like science fiction.</em></li>
          <li><em>She loves reading fantasy books.</em></li>
          <li><em>He hates waking up early.</em></li>
          <li><em>We like playing RPG.</em></li>
          <li><em>They don't like horror movies.</em></li>
        </ul>

        <div class="tip">
          <strong>Para conversa:</strong> pergunte <em>What kind of movies do you like?</em> ou <em>Do you like playing games?</em>
        </div>
      </div>

