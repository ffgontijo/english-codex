      <div class="section-head">
        <h2>Conteúdos essenciais do A1</h2>
        <p>
          Estes são os blocos que transformam o inglês inicial em frases úteis. O segredo é dominar bem o simples. Sim, o simples. Aquilo que todo mundo quer pular e depois passa anos errando.
        </p>
