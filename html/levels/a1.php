      <div class="grammar-block">
        <h3>1 · Verb to be: identidade, descrição e estado</h3>

        <p>
          O verbo <em>to be</em> é usado para falar de identidade, profissão, origem, idade, estado, descrição e localização.
        </p>

        <div class="formula">
          I am | You are | He is | She is | It is | We are | They are
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I am Brazilian.</em></li>
          <li><em>She is a teacher.</em></li>
          <li><em>He is tired.</em></li>
          <li><em>They are students.</em></li>
          <li><em>The city is beautiful.</em></li>
          <li><em>The movie is interesting.</em></li>
        </ul>

        <div class="warn">
          <strong>Português te trai aqui:</strong> idade em inglês usa <em>to be</em>. Diga <em>I am 26 years old</em>, não <em>I have 26 years</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>2 · To be: negativa e perguntas</h3>

        <p>
          Para negar com <em>to be</em>, usamos <em>not</em>. Para perguntar, colocamos o verbo antes do sujeito.
        </p>

        <div class="formula">
          Subject + am/is/are + not | Am/Is/Are + subject...?
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I am not tired.</em></li>
          <li><em>She is not from Canada.</em></li>
          <li><em>They are not at home.</em></li>
          <li><em>Are you a student?</em></li>
          <li><em>Is he your brother?</em></li>
          <li><em>Are they ready?</em></li>
        </ul>

        <div class="tip">
          <strong>Contrações úteis:</strong> <em>I'm</em>, <em>you're</em>, <em>he's</em>, <em>she's</em>, <em>it's</em>, <em>we're</em>, <em>they're</em>, <em>isn't</em>, <em>aren't</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>3 · Present Simple: rotina e fatos gerais</h3>

        <p>
          O <em>Present Simple</em> é usado para falar de rotina, hábitos, fatos, preferências e coisas que acontecem normalmente.
        </p>

        <div class="formula">
          I / You / We / They + base verb | He / She / It + verb + -s
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I work in IT.</em></li>
          <li><em>You study English.</em></li>
          <li><em>We live in Brazil.</em></li>
          <li><em>They play board games on weekends.</em></li>
          <li><em>She reads every night.</em></li>
          <li><em>He watches science fiction movies.</em></li>
        </ul>

        <div class="warn">
          <strong>Erro clássico:</strong> não diga <em>She work</em>. Com <em>he/she/it</em>, use <em>-s</em>: <em>She works</em>. Pequeno <em>s</em>, grande humilhação.
        </div>
      </div>

      <div class="grammar-block">
        <h3>4 · Present Simple: negativas</h3>

        <p>
          Para negar no <em>Present Simple</em>, usamos <em>don't</em> e <em>doesn't</em>. Depois deles, o verbo fica na forma base.
        </p>

        <div class="formula">
          I / You / We / They + don't + verb | He / She / It + doesn't + verb
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I don't drink coffee at night.</em></li>
          <li><em>They don't work on Sunday.</em></li>
          <li><em>We don't understand this word.</em></li>
          <li><em>She doesn't like horror movies.</em></li>
          <li><em>He doesn't play RPG every week.</em></li>
          <li><em>The app doesn't work well.</em></li>
        </ul>

        <div class="warn">
          <strong>Erro comum:</strong> não diga <em>She doesn't likes</em>. O <em>doesn't</em> já carrega a terceira pessoa. O verbo volta ao normal: <em>She doesn't like</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>5 · Present Simple: perguntas</h3>

        <p>
          Para fazer perguntas no <em>Present Simple</em>, usamos <em>do</em> ou <em>does</em> antes do sujeito.
        </p>

        <div class="formula">
          Do / Does + subject + base verb?
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>Do you study English every day?</em></li>
          <li><em>Do they live near here?</em></li>
          <li><em>Does she work in marketing?</em></li>
          <li><em>Does he like science fiction?</em></li>
          <li><em>Does this game have a solo mode?</em></li>
        </ul>

        <div class="tip">
          <strong>Resposta curta:</strong> <em>Yes, I do.</em> / <em>No, I don't.</em> / <em>Yes, she does.</em> / <em>No, she doesn't.</em>
        </div>
      </div>

