      <div class="grammar-block">
        <h3>9 · There is / There are</h3>

        <p>
          Use <em>there is</em> e <em>there are</em> para dizer que algo existe em algum lugar.
        </p>

        <div class="formula">
          There is + singular | There are + plural
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>There is a book on the table.</em></li>
          <li><em>There is a problem with the app.</em></li>
          <li><em>There are three students in the room.</em></li>
          <li><em>There are many stars in the sky.</em></li>
          <li><em>There is a monster behind the door.</em></li>
        </ul>

        <div class="warn">
          <strong>Português te trai aqui:</strong> “tem um livro na mesa” não vira <em>has a book on the table</em>. Use <em>There is a book on the table.</em>
        </div>
      </div>

