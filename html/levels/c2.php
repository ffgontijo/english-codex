      <div class="table-wrap">
        <table>
          <caption>Erros comuns de brasileiros no C2</caption>

          <thead>
            <tr>
              <th>Problema</th>
              <th>Forma melhor</th>
              <th>Explicação</th>
            </tr>
          </thead>

          <tbody>
            <tr>
              <td><em>This discussion is very polemic.</em></td>
              <td><em>This issue is highly controversial.</em></td>
              <td><em>Polemic</em> existe, mas <em>controversial</em> é mais natural nesse uso.</td>
            </tr>

            <tr>
              <td><em>The text brings an important reflection.</em></td>
              <td><em>The text raises an important point.</em></td>
              <td>Evite calque do português. Inglês usa <em>raise a point/question/concern</em>.</td>
            </tr>

            <tr>
              <td><em>In a first moment...</em></td>
              <td><em>At first...</em> / <em>Initially...</em></td>
              <td>Tradução literal de “em um primeiro momento”. Esse fantasma ama brasileiros.</td>
            </tr>

            <tr>
              <td><em>This author makes a criticism about society.</em></td>
              <td><em>The author criticises society.</em> / <em>The author offers a critique of society.</em></td>
              <td>Escolha estrutura natural: verbo direto ou collocation correta.</td>
            </tr>

            <tr>
              <td><em>The results were much expressive.</em></td>
              <td><em>The results were highly significant.</em></td>
              <td><em>Expressive</em> raramente funciona como “expressivo” em português.</td>
            </tr>

            <tr>
              <td><em>This can generate problems.</em></td>
              <td><em>This can cause problems.</em> / <em>This may give rise to problems.</em></td>
              <td><em>Generate</em> é possível em alguns contextos, mas muitas vezes soa como tradução.</td>
            </tr>

            <tr>
              <td><em>It is important to highlight that...</em></td>
              <td><em>It is worth noting that...</em></td>
              <td>Mais natural para introduzir ponto relevante em escrita formal.</td>
            </tr>

            <tr>
              <td><em>The character passes through a transformation.</em></td>
              <td><em>The character undergoes a transformation.</em></td>
              <td>Collocation natural: <em>undergo a transformation</em>.</td>
            </tr>
          </tbody>
        </table>
