      <div class="grammar-block">
        <h3>9 · Third Conditional</h3>
        <p> Use o <em>Third Conditional</em> para imaginar um resultado diferente no passado. Ele é ótimo para arrependimentos, consequências, análise de decisões e aquele passatempo humano de sofrer por possibilidades que não existem mais. </p>
        <div class="formula"> If + Past Perfect, would have + past participle </div>
        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li>
            <em>If I had studied more, I would have passed the test.</em>
          </li>
          <li>
            <em>If she had saved the file, she wouldn't have lost her work.</em>
          </li>
          <li>
            <em>If the hero had listened to the warning, he would have avoided the trap.</em>
          </li>
          <li>
            <em>If the team had checked the data earlier, they would have found the mistake.</em>
          </li>
        </ul>
        <div class="warn">
          <strong>Cuidado:</strong> o <em>Third Conditional</em> fala de passado imaginário. Não use para plano real de futuro. </div>
      </div>
      <div class="grammar-block">
        <h3>10 · Mixed Conditionals</h3>
        <p> Mixed conditionals misturam tempos para conectar uma condição passada a um resultado presente, ou uma condição presente a um resultado passado. </p>
        <div class="formula"> If + Past Perfect, would + base verb </div>
        <ul class="ex-list">
          <li>
            <em>If I had learned English earlier, I would be more confident now.</em>
          </li>
          <li>
            <em>If she had accepted the job, she would live in Canada now.</em>
          </li>
          <li>
            <em>If the character had made a different choice, the story would be completely different.</em>
          </li>
        </ul>
        <div class="tip">
          <strong>Uso avançado para B2:</strong> não precisa usar o tempo todo, mas precisa reconhecer e entender quando aparece. </div>
