          </li>
          <li>
            <em>They have been watching documentaries to improve their listening.</em>
          </li>
          <li>
            <em>The players have been exploring the ruins for two hours.</em>
          </li>
          <li>
            <em>He looks tired because he has been studying all night.</em>
          </li>
        </ul>
