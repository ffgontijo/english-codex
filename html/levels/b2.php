        <h3>6 · Reported Speech</h3>
        <p> Use <em>Reported Speech</em> para relatar o que alguém disse. Ele aparece em histórias, reuniões, notícias, investigações, e-mails e fofocas corporativas, esse gênero literário subestimado. </p>
        <div class="formula"> said / told + reported sentence </div>
        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li>
            <em>Claire said she was working on the article.</em>
          </li>
          <li>
            <em>He told me he had already sent the file.</em>
          </li>
          <li>
            <em>The witness said that she had seen a strange light.</em>
          </li>
          <li>
            <em>The manager told us that the meeting would start later.</em>
          </li>
        </ul>
        <div class="warn">
          <strong>Say vs tell:</strong> use <em>told me</em>, <em>told us</em>, <em>told the team</em>. Não diga <em>said me</em>. O inglês já tem pegadinhas suficientes sem você inventar novas. </div>
      </div>
      <div class="grammar-block">
        <h3>7 · Reported Questions</h3>
        <p> Perguntas relatadas não mantêm a ordem de pergunta. Elas viram estrutura afirmativa. </p>
        <div class="formula"> asked + question word + subject + verb </div>
        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li>
            <em>She asked where I lived.</em>
          </li>
          <li>
            <em>He asked what time the class started.</em>
          </li>
          <li>
            <em>They asked if I had finished the report.</em>
          </li>
          <li>
            <em>The detective asked why the door was open.</em>
          </li>
        </ul>
        <div class="warn">
          <strong>Erro comum:</strong> não diga <em>She asked where did I live</em>. Diga <em>She asked where I lived</em>. </div>
      </div>
