      <div class="section-head">
        <h2>Conteúdos essenciais do B1</h2>
        <p>
          O B1 organiza o que o aluno já sabe e adiciona ferramentas para narrar, argumentar, conectar ideias e falar de experiências. É aqui que o inglês começa a ter corpo. Ainda meio desengonçado, mas corpo.
        </p>
      </div>

      <div class="grammar-block">
        <h3>1 · Present Perfect: experiências e conexão com o presente</h3>

        <p>
          O <em>Present Perfect</em> conecta passado e presente. Ele aparece para falar de experiências, ações recentes, mudanças e situações que começaram no passado e continuam agora.
        </p>

        <div class="formula">
          have / has + past participle
        </div>

        <h4>Usos principais</h4>
        <ul class="ex-list">
          <li><em>I have visited that museum twice.</em></li>
          <li><em>She has never played a tabletop RPG.</em></li>
          <li><em>Have you ever watched a movie without subtitles?</em></li>
          <li><em>They have already finished the report.</em></li>
          <li><em>He has worked here since 2021.</em></li>
        </ul>

        <div class="warn">
          <strong>Português te trai aqui:</strong> “Eu moro aqui há três anos” não vira <em>I live here since three years</em>. Diga <em>I have lived here for three years</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>2 · Present Perfect vs Past Simple</h3>

        <p>
          Use <em>Past Simple</em> quando o tempo passado está claro ou terminado. Use <em>Present Perfect</em> quando a experiência ou resultado tem conexão com o presente.
        </p>

        <div class="table-wrap">
          <table>
            <caption>Present Perfect vs Past Simple</caption>
            <thead>
              <tr>
                <th>Ideia</th>
                <th>Past Simple</th>
                <th>Present Perfect</th>
              </tr>
            </thead>

            <tbody>
              <tr>
                <td>Tempo terminado</td>
                <td><em>I watched that movie yesterday.</em></td>
                <td><em>I have watched that movie.</em></td>
              </tr>

              <tr>
                <td>Experiência</td>
                <td><em>I went to Canada in 2022.</em></td>
                <td><em>I have been to Canada.</em></td>
              </tr>

              <tr>
                <td>Resultado atual</td>
                <td><em>She lost her keys last night.</em></td>
                <td><em>She has lost her keys.</em></td>
              </tr>

              <tr>
                <td>Duração até agora</td>
                <td><em>I lived there in 2020.</em></td>
                <td><em>I have lived here since 2020.</em></td>
              </tr>
            </tbody>
          </table>
        </div>

        <div class="tip">
          <strong>Regra prática:</strong> se você está dizendo <em>when</em>, normalmente usa <em>Past Simple</em>. Se está falando de experiência ou resultado, provavelmente usa <em>Present Perfect</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>3 · For, since, already, yet and ever</h3>

        <p>
          Estas palavras aparecem muito com <em>Present Perfect</em>. Elas ajudam a mostrar duração, início, experiência, conclusão e expectativa.
        </p>

        <div class="table-wrap">
          <table>
            <caption>Palavras-chave com Present Perfect</caption>
            <thead>
              <tr>
                <th>Palavra</th>
                <th>Uso</th>
                <th>Exemplo</th>
              </tr>
            </thead>

            <tbody>
              <tr>
                <td>for</td>
                <td>duração</td>
                <td><em>I have studied English for two years.</em></td>
              </tr>

              <tr>
                <td>since</td>
                <td>ponto de início</td>
                <td><em>She has worked here since 2020.</em></td>
              </tr>

              <tr>
                <td>already</td>
                <td>algo que já aconteceu</td>
                <td><em>They have already finished the mission.</em></td>
              </tr>

              <tr>
                <td>yet</td>
                <td>ainda / já, em negativas e perguntas</td>
                <td><em>Have you finished yet?</em></td>
              </tr>

              <tr>
                <td>ever</td>
                <td>alguma vez</td>
                <td><em>Have you ever played Dungeons & Dragons?</em></td>
              </tr>

              <tr>
                <td>never</td>
                <td>nunca</td>
                <td><em>I have never read that book.</em></td>
              </tr>
            </tbody>
          </table>
        </div>
      </div>

      <div class="grammar-block">
        <h3>4 · Past Continuous</h3>

        <p>
          Use <em>Past Continuous</em> para falar de uma ação em progresso no passado. Ele é ótimo para narrativas, interrupções e cenas.
        </p>

        <div class="formula">
          was / were + verb + -ing
        </div>

        <h4>Exemplos</h4>
        <ul class="ex-list">
          <li><em>I was studying when you called.</em></li>
          <li><em>She was watching a movie when the power went out.</em></li>
          <li><em>The detective was looking at the window when he heard a noise.</em></li>
          <li><em>They were walking through the forest when they found an old map.</em></li>
        </ul>

        <div class="tip">
          <strong>Uso narrativo:</strong> o <em>Past Continuous</em> cria a cena; o <em>Past Simple</em> mostra o evento que acontece dentro dela.
        </div>
      </div>

      <div class="grammar-block">
        <h3>5 · Past Simple + Past Continuous</h3>

        <p>
          Esta combinação é uma das melhores ferramentas para contar histórias em inglês.
        </p>

        <div class="formula">
          Past Continuous para contexto | Past Simple para ação principal
        </div>

        <ul class="ex-list">
          <li><em>I was reading when the phone rang.</em></li>
          <li><em>The crew was exploring the ship when the alarm started.</em></li>
          <li><em>She was writing a report when she noticed a mistake.</em></li>
          <li><em>The players were planning their next move when the monster appeared.</em></li>
        </ul>

        <div class="warn">
          <strong>Erro comum:</strong> não use <em>was</em> em tudo só porque é passado. <em>I was watched a movie</em> está errado. Diga <em>I watched a movie</em> ou <em>I was watching a movie</em>, dependendo da ideia.
        </div>
      </div>

      <div class="grammar-block">
        <h3>6 · Used to</h3>

        <p>
          Use <em>used to</em> para falar de hábitos ou situações do passado que não são mais verdade agora.
        </p>

        <div class="formula">
          subject + used to + base verb
        </div>

        <ul class="ex-list">
          <li><em>I used to play video games every night.</em></li>
          <li><em>She used to live in a small town.</em></li>
          <li><em>We used to watch cartoons on Saturday mornings.</em></li>
          <li><em>He didn't use to like horror movies.</em></li>
          <li><em>Did you use to study English at school?</em></li>
        </ul>

        <div class="tip">
          <strong>Para o caderno:</strong> escreva cinco frases sobre coisas que você fazia antes e não faz mais.
        </div>
      </div>

      <div class="grammar-block">
        <h3>7 · First Conditional</h3>

        <p>
          Use o <em>First Conditional</em> para falar de possibilidades reais no futuro.
        </p>

        <div class="formula">
          If + Present Simple, will + base verb
        </div>

        <ul class="ex-list">
          <li><em>If I study tonight, I will understand the lesson better.</em></li>
          <li><em>If it rains, we will stay home.</em></li>
          <li><em>If the team finds the clue, they will solve the mystery.</em></li>
          <li><em>If you practice speaking, you will become more confident.</em></li>
        </ul>

        <div class="warn">
          <strong>Erro comum:</strong> não diga <em>If I will study...</em>. Depois de <em>if</em>, use presente: <em>If I study...</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>8 · Second Conditional</h3>

        <p>
          Use o <em>Second Conditional</em> para situações imaginárias, hipotéticas ou pouco prováveis no presente ou futuro.
        </p>

        <div class="formula">
          If + Past Simple, would + base verb
        </div>

        <ul class="ex-list">
          <li><em>If I had more time, I would study every day.</em></li>
          <li><em>If I were a wizard, I would live in a tower.</em></li>
          <li><em>If she knew the answer, she would tell us.</em></li>
          <li><em>If the captain trusted the crew, the mission would be easier.</em></li>
        </ul>

        <div class="tip">
          <strong>Uso com cultura pop:</strong> ótimo para imaginar escolhas de personagens: <em>If I were the hero, I would not open that door.</em>
        </div>
      </div>

      <div class="grammar-block">
        <h3>9 · Modals: should, must, have to, might</h3>

        <p>
          Modais ajudam a falar de conselho, obrigação, necessidade e possibilidade.
        </p>

        <div class="table-wrap">
          <table>
            <caption>Modais úteis no B1</caption>
            <thead>
              <tr>
                <th>Modal</th>
                <th>Função</th>
                <th>Exemplo</th>
              </tr>
            </thead>

            <tbody>
              <tr>
                <td>should</td>
                <td>conselho</td>
                <td><em>You should review your notes.</em></td>
              </tr>

              <tr>
                <td>must</td>
                <td>obrigação forte</td>
                <td><em>You must wear a helmet here.</em></td>
              </tr>

              <tr>
                <td>have to</td>
                <td>obrigação externa</td>
                <td><em>I have to finish this report today.</em></td>
              </tr>

              <tr>
                <td>might</td>
                <td>possibilidade</td>
                <td><em>The suspect might be lying.</em></td>
              </tr>
            </tbody>
          </table>
        </div>

        <div class="warn">
          <strong>Importante:</strong> depois de modal, o verbo fica na forma base: <em>She should study</em>, não <em>She should studies</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>10 · Gerunds and infinitives</h3>

        <p>
          Alguns verbos são seguidos por <em>-ing</em>; outros por <em>to + verb</em>. Isso é uma daquelas coisas que inglês faz para manter a espécie humana humilde.
        </p>

        <div class="cards two-col">
          <article class="card">
            <h3>Verb + -ing</h3>
            <ul class="check-list">
              <li><em>I enjoy reading fantasy books.</em></li>
              <li><em>She avoids watching horror movies.</em></li>
              <li><em>They finished writing the report.</em></li>
              <li><em>We discussed learning English online.</em></li>
            </ul>
          </article>

          <article class="card">
            <h3>Verb + to</h3>
            <ul class="check-list">
              <li><em>I want to improve my speaking.</em></li>
              <li><em>She decided to study abroad.</em></li>
              <li><em>They need to leave early.</em></li>
              <li><em>He hopes to find a better job.</em></li>
            </ul>
          </article>
        </div>

        <div class="tip">
          <strong>Para o caderno:</strong> crie uma lista pessoal de verbos que você usa muito: <em>want to</em>, <em>need to</em>, <em>enjoy -ing</em>, <em>avoid -ing</em>.
        </div>
      </div>

      <div class="grammar-block">
        <h3>11 · Relative clauses</h3>

        <p>
          Relative clauses conectam ideias e evitam frases muito quebradas. Use <em>who</em> para pessoas, <em>which</em> para coisas e <em>where</em> para lugares.
        </p>

        <div class="formula">
          who = pessoa | which = coisa | where = lugar
        </div>

        <ul class="ex-list">
          <li><em>This is the teacher who helped me.</em></li>
          <li><em>This is the book which changed my opinion.</em></li>
          <li><em>This is the city where I grew up.</em></li>
          <li><em>The detective found a clue which explained the case.</em></li>
          <li><em>She met a character who never tells the truth.</em></li>
        </ul>
      </div>

      <div class="grammar-block">
        <h3>12 · Too and enough</h3>

        <p>
          Use <em>too</em> para excesso e <em>enough</em> para suficiência.
        </p>

        <div class="formula">
          too + adjective | adjective + enough | enough + noun
        </div>

        <ul class="ex-list">
          <li><em>This text is too difficult.</em></li>
          <li><em>The room is too small for ten people.</em></li>
          <li><em>This explanation is clear enough.</em></li>
          <li><em>We don't have enough time.</em></li>
          <li><em>The hero isn't strong enough to open the gate.</em></li>
        </ul>
      </div>

      <div class="grammar-block">
        <h3>13 · Connecting ideas</h3>

        <p>
          No B1, o aluno precisa conectar ideias com mais clareza. Sem conectores, o texto vira uma fila de frases esperando atendimento.
        </p>

        <div class="table-wrap">
          <table>
            <caption>Conectores úteis no B1</caption>
            <thead>
              <tr>
                <th>Função</th>
                <th>Conectores</th>
                <th>Exemplo</th>
              </tr>
            </thead>

            <tbody>
              <tr>
                <td>adição</td>
                <td>also, too, as well</td>
                <td><em>I like the story. I also like the characters.</em></td>
              </tr>

              <tr>
                <td>contraste</td>
                <td>but, however, although</td>
                <td><em>The movie is long, but it is interesting.</em></td>
              </tr>

              <tr>
                <td>motivo</td>
                <td>because, because of</td>
                <td><em>I stayed home because I was tired.</em></td>
              </tr>

              <tr>
                <td>resultado</td>
                <td>so, therefore</td>
                <td><em>It was raining, so we cancelled the trip.</em></td>
              </tr>

              <tr>
                <td>sequência</td>
                <td>first, then, after that, finally</td>
                <td><em>First, we entered the room. Then, we found the key.</em></td>
              </tr>
            </tbody>
          </table>
        </div>
      </div>

      <div class="grammar-block">
        <h3>14 · Giving opinions</h3>

        <p>
          No B1, opinião precisa ser mais do que “I like it”. Você precisa dizer o que pensa e explicar por quê. Um passo perigoso rumo à civilização.
        </p>

        <ul class="ex-list">
          <li><em>In my opinion, the ending was too predictable.</em></li>
          <li><em>I think the story works because the characters are believable.</em></li>
          <li><em>I agree with the idea, but I don't agree with the solution.</em></li>
          <li><em>Personally, I prefer books that focus on mystery and character development.</em></li>
          <li><em>The game is fun, although some missions are repetitive.</em></li>
        </ul>

        <div class="tip">
          <strong>Estrutura útil:</strong> opinião + motivo + exemplo. Sem isso, a opinião fica parecendo comentário perdido.
        </div>
      </div>

      <div class="grammar-block">
        <h3>15 · Storytelling</h3>

        <p>
          Storytelling no B1 é contar uma história simples com começo, desenvolvimento e fim. Pode ser algo real, uma cena de filme, uma sessão de RPG ou uma situação inventada.
        </p>

        <div class="formula">
          setting → problem → action → result → reaction
        </div>

        <ul class="ex-list">
          <li><em>Last Saturday, I played a board game with my friends.</em></li>
          <li><em>At first, I didn't understand the rules.</em></li>
          <li><em>Then, I made a risky decision.</em></li>
          <li><em>In the end, I lost the game, but I learned a better strategy.</em></li>
          <li><em>It was frustrating, but also very fun.</em></li>
        </ul>
      </div>
    </div>
