<section class="section paper">
  <div class="container">
    <div class="section-head">
      <h2>Gramática que engana brasileiros</h2>
      <p>
        Alguns pontos parecem simples até você tentar usar em frase real. Aí o português entra na sala, senta no teclado e começa a tomar decisões.
      </p>
    </div>

    <div class="cards three-col">
      <article class="card">
        <h3>There is / There are</h3>
        <p>
          Em português dizemos “tem uma pessoa aqui”. Em inglês, para existência, o padrão é <em>there is</em> ou <em>there are</em>.
        </p>
        <ul class="ex-list">
          <li><em>There is a problem.</em></li>
          <li><em>There are three people here.</em></li>
          <li><em>There was a meeting yesterday.</em></li>
        </ul>
      </article>

      <article class="card">
        <h3>Do / Make</h3>
        <p>
          Os dois podem parecer “fazer”, mas não são intercambiáveis. Inglês gosta de combinações fixas.
        </p>
        <ul class="ex-list">
          <li><em>do homework</em></li>
          <li><em>do business</em></li>
          <li><em>make a mistake</em></li>
          <li><em>make a decision</em></li>
        </ul>
      </article>

      <article class="card">
        <h3>Say / Tell</h3>
        <p>
          Use <em>tell</em> quando mencionar a pessoa que recebe a informação. Use <em>say</em> para focar na fala em si.
        </p>
        <ul class="ex-list">
          <li><em>She told me the truth.</em></li>
          <li><em>She said the truth.</em> soa incomum.</li>
          <li><em>She said that she was tired.</em></li>
        </ul>
      </article>

      <article class="card">
        <h3>To be em excesso</h3>
        <p>
          Brasileiro coloca <em>am/is/are</em> onde não precisa porque tenta traduzir “estou” ou “sou” automaticamente.
        </p>
        <ul class="ex-list">
          <li><em>I agree.</em>, não <em>I am agree.</em></li>
          <li><em>I like it.</em>, não <em>I am like it.</em></li>
          <li><em>It depends.</em>, não <em>It is depends.</em></li>
        </ul>
      </article>

      <article class="card">
        <h3>Articles: a, an, the</h3>
        <p>
          Inglês usa artigos de forma diferente do português. Às vezes precisa de artigo onde português não usa; às vezes não usa onde português usaria.
        </p>
        <ul class="ex-list">
          <li><em>I am a data analyst.</em></li>
          <li><em>I like music.</em>, não <em>the music</em> em sentido geral.</li>
          <li><em>The book on the table</em> indica algo específico.</li>
        </ul>
      </article>

      <article class="card">
        <h3>Present Perfect</h3>
        <p>
          O <em>present perfect</em> é difícil porque não encaixa perfeitamente no português. Ele conecta passado e presente.
        </p>
        <ul class="ex-list">
          <li><em>I have studied English for years.</em></li>
          <li><em>She has already finished.</em></li>
          <li><em>Have you ever been to Canada?</em></li>
        </ul>
      </article>
    </div>
