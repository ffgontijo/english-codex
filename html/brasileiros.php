<section class="section">
  <div class="container">
    <div class="section-head">
      <h2>Pronúncia para brasileiros</h2>
      <p>
        Pronúncia não é “falar sem sotaque”. O objetivo é ser claro, compreensível e mais natural. Sotaque não é crime; falta de clareza é que atrapalha.
      </p>
    </div>

    <div class="cards three-col">
      <article class="card">
        <h3>/θ/ e /ð/ — th</h3>
        <p>
          Os sons de <em>think</em>, <em>three</em>, <em>this</em> e <em>that</em> não existem em português. A ponta da língua toca levemente os dentes superiores.
        </p>
        <p>
          <strong>Dica:</strong> para <em>think</em>, deixe o ar passar sem voz. Para <em>this</em>, use voz.
        </p>
      </article>

      <article class="card">
        <h3>Consoantes finais</h3>
        <p>
          Brasileiro tende a adicionar uma vogal no final: <em>work</em> vira “worki”, <em>book</em> vira “booki”. Em inglês, a palavra precisa terminar na consoante.
        </p>
        <p>
          <strong>Dica:</strong> pense na consoante final como uma porta fechando. Não coloque uma vogal depois.
        </p>
      </article>

      <article class="card">
        <h3>Word stress</h3>
        <p>
          A sílaba forte muda a música da palavra inteira. Errar o stress pode confundir mais que errar uma vogal.
        </p>
        <ul class="ex-list">
          <li><em>PHOtograph</em> vs <em>phoTOGraphy</em></li>
          <li><em>PREsent</em> substantivo vs <em>preSENT</em> verbo</li>
          <li><em>REcord</em> substantivo vs <em>reCORD</em> verbo</li>
        </ul>
      </article>

      <article class="card">
        <h3>Vogais curtas e longas</h3>
        <p>
          Pares como <em>ship/sheep</em>, <em>bit/beat</em> e <em>full/fool</em> exigem atenção. Não é drama, é diferença de significado.
        </p>
      </article>

      <article class="card">
        <h3>H inicial</h3>
        <p>
          Em palavras como <em>house</em>, <em>happy</em> e <em>help</em>, o <em>h</em> precisa sair com ar. Não é “ouse”, “appy” e “elp”, por mais econômico que pareça.
        </p>
      </article>

      <article class="card">
        <h3>Ritmo da frase</h3>
        <p>
          Inglês tem ritmo mais marcado por palavras fortes. Nem todas as palavras recebem o mesmo peso. Isso afeta listening e speaking.
        </p>
      </article>
    </div>
  </div>
</section>

