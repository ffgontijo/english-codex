<section class="section paper">
  <div class="container">
    <div class="section-head">
      <h2>Exemplos por nível: o mesmo universo, mais fundo</h2>
      <p>
        O mesmo tema pode aparecer em A1 e em C2. A diferença está na complexidade da estrutura, no vocabulário e na profundidade da análise.
      </p>
    </div>

    <div class="grammar-block">
      <h3>A0-A1 · Identidade, rotina e descrição</h3>
      <p>
        Neste nível, cultura pop ajuda a criar imagens simples: personagem, lugar, objeto, rotina e gosto pessoal.
      </p>

      <ul class="ex-list">
        <li><em>The detective is quiet. He works at night.</em> — verb to be + Present Simple</li>
        <li><em>The wizard lives in a tower. He studies old books.</em> — rotina</li>
        <li><em>My favorite movie is about space. I like the robots.</em> — gosto pessoal</li>
        <li><em>The ship is old, but it is fast.</em> — descrição simples</li>
      </ul>

      <div class="tip">
        <strong>Para o caderno:</strong> crie cinco personagens simples e escreva duas frases sobre cada um.
      </div>
    </div>

    <div class="grammar-block">
      <h3>A2 · Passado, futuro e comparação</h3>
      <p>
        Aqui entram pequenas narrativas: o que aconteceu, o que vai acontecer e como comparar opções.
      </p>

      <ul class="ex-list">
        <li><em>The explorer found an old map and decided to follow it.</em> — Past Simple</li>
        <li><em>The crew is going to investigate the signal tomorrow.</em> — going to</li>
        <li><em>This route is shorter but more dangerous than the old road.</em> — comparative</li>
        <li><em>Last night, the investigator discovered three clues.</em> — sequência no passado</li>
      </ul>

      <div class="tip">
        <strong>Para o caderno:</strong> escreva um resumo de cinco frases sobre uma missão, episódio ou aventura fictícia.
      </div>
    </div>

    <div class="grammar-block">
      <h3>B1 · Experiência, opinião e hipótese</h3>
      <p>
        O aluno começa a interpretar e argumentar. RPG, séries e ficção são ótimos para falar de escolhas, experiências e possibilidades.
      </p>

      <ul class="ex-list">
        <li><em>I have never played a tabletop RPG, but I have always wanted to try.</em> — Present Perfect</li>
        <li><em>If I were the captain, I would check the archives first.</em> — Second Conditional</li>
        <li><em>In my opinion, the best stories are the ones where the hero fails before succeeding.</em> — opinião estruturada</li>
        <li><em>The detective has interviewed four witnesses, but she hasn't found solid evidence yet.</em> — Present Perfect com yet</li>
      </ul>

      <div class="tip">
        <strong>Para o caderno:</strong> escreva três opiniões sobre histórias, personagens ou jogos usando <em>in my opinion</em>, <em>I believe</em> e <em>however</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>B2 · Argumentação, dedução e análise</h3>
      <p>
        No B2, cultura pop deixa de ser só cenário e vira objeto de análise. O aluno precisa justificar ideias com clareza.
      </p>

      <ul class="ex-list">
        <li><em>The station was built by an unknown civilisation long before the first expedition arrived.</em> — Passive Voice</li>
        <li><em>The witness said that she had seen a light in the corridor.</em> — Reported Speech</li>
        <li><em>If the captain had checked the files earlier, the crew would have avoided the anomaly.</em> — Third Conditional</li>
        <li><em>The antagonist must have known the password; no one else could have accessed the system.</em> — modal deduction</li>
      </ul>

      <div class="tip">
        <strong>Para o caderno:</strong> escolha uma cena e escreva uma análise curta: contexto, conflito, evidência e interpretação.
      </div>
    </div>

    <div class="grammar-block">
      <h3>C1 · Nuance, registro e subtexto</h3>
      <p>
        Aqui o aluno trabalha precisão, estilo e interpretação. Não basta dizer o que aconteceu; é preciso explicar como a história cria significado.
      </p>

      <ul class="ex-list">
        <li><em>It could be argued that the villain's politeness is not incidental, but central to the scene's tension.</em> — hedging + análise</li>
        <li><em>Never has such a minor character carried so much thematic weight.</em> — inversion</li>
        <li><em>The unresolved ending appears to reject the comfort of easy closure.</em> — registro formal</li>
        <li><em>Not only does the plot subvert genre expectations, but it also questions the assumptions behind them.</em> — not only inversion</li>
      </ul>

      <div class="tip">
        <strong>Para o caderno:</strong> crie uma lista de frases para análise: <em>It could be argued that...</em>, <em>This suggests that...</em>, <em>The scene appears to...</em>.
      </div>
    </div>

    <div class="grammar-block">
      <h3>C2 · Estilo, retórica e voz</h3>
      <p>
        No C2, o foco é forma, tom, ritmo, ironia e controle fino da linguagem. O aluno analisa não só o que a história diz, mas o que ela evita dizer.
      </p>

      <ul class="ex-list">
        <li><em>The scene works not because of what is said, but because of what is withheld.</em> — subtexto</li>
        <li><em>The villain is terrifying precisely because he sounds reasonable.</em> — análise retórica</li>
        <li><em>The further the protagonist goes, the less she understands herself.</em> — parallelism</li>
        <li><em>Calling that decision “brave” would be generous; calling it reckless would be accurate.</em> — contraste estilístico</li>
      </ul>

      <div class="tip">
        <strong>Para o caderno:</strong> escreva a mesma opinião em três estilos: neutro, acadêmico e irônico.
      </div>
    </div>
  </div>
</section>

